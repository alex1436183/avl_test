try:
    # Запрашиваем первое число
    number1 = float(input("Введите первое число: "))

    # Запрашиваем второе число
    number2 = float(input("Введите второе число: "))

    # Если оба числа введены корректно, вычисляем произведение
    product = number1 * number2

    # Выводим результат
    print(f"Произведение {number1} и {number2} равно {product}")

except ValueError:
    print("Ошибка: оба значения должны быть числами!")